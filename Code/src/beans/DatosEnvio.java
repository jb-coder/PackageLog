package beans;

public class DatosEnvio {
	
	private String nenvio;
	private String cliente;
	private String cpOrigen;
	private String localidadOrigen;
	private String cpDestino ;
	private String localidadDestino;
	private String destinatario ;
	private String direccion ;
	private String observaciones;
	private Double peso;
	private Double volumen;
	public String getNenvio() {
		return nenvio;
	}
	public void setNenvio(String nenvio) {
		this.nenvio = nenvio;
	}
	public String getCliente() {
		return cliente;
	}
	public void setCliente(String cliente) {
		this.cliente = cliente;
	}
	public String getCpOrigen() {
		return cpOrigen;
	}
	public void setCpOrigen(String cpOrigen) {
		this.cpOrigen = cpOrigen;
	}
	public String getLocalidadOrigen() {
		return localidadOrigen;
	}
	public void setLocalidadOrigen(String localidadOrigen) {
		this.localidadOrigen = localidadOrigen;
	}
	public String getCpDestino() {
		return cpDestino;
	}
	public void setCpDestino(String cpDestino) {
		this.cpDestino = cpDestino;
	}
	public String getLocalidadDestino() {
		return localidadDestino;
	}
	public void setLocalidadDestino(String localidadDestino) {
		this.localidadDestino = localidadDestino;
	}
	public String getDestinatario() {
		return destinatario;
	}
	public void setDestinatario(String destinatario) {
		this.destinatario = destinatario;
	}
	public String getDireccion() {
		return direccion;
	}
	public void setDireccion(String direccion) {
		this.direccion = direccion;
	}
	public String getObservaciones() {
		return observaciones;
	}
	public void setObservaciones(String observaciones) {
		this.observaciones = observaciones;
	}
	public Double getPeso() {
		return peso;
	}
	public void setPeso(Double peso) {
		this.peso = peso;
	}
	public Double getVolumen() {
		return volumen;
	}
	public void setVolumen(Double volumen) {
		this.volumen = volumen;
	}
	
	

}
